package logica_de_programacao.outros;

public class BitWise {
    public static void main(String[] args) {
        int X = 89;
        int Z = 60;

        System.out.println(X & Z); //Operador E
        System.out.println(X | Z); //Operador OU
        System.out.println(X ^ Z); //Operador OU Exclusivo
    }
}
